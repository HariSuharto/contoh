# Hitung Luas Segitiga sederhana
def luas_segitiga():
    a= int(input("Masukan Alas Segitiga: "))
    t= int(input("Masukan tinggi segitiga: "))
    luas = a * t / 2
    print("Luas segitiga adalah: ", luas)

luas_segitiga()

